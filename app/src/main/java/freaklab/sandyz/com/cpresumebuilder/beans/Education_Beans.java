package freaklab.sandyz.com.cpresumebuilder.beans;

public class Education_Beans {


    int ID;
    String college_name;
    String major;
    String edu_from;
    String edu_to;

    public int getID() {
        return ID;
    }

    public void setID(int ID) {
        this.ID = ID;
    }

    public String getCollege_name() {
        return college_name;
    }

    public void setCollege_name(String college_name) {
        this.college_name = college_name;
    }

    public String getMajor() {
        return major;
    }

    public void setMajor(String major) {
        this.major = major;
    }

    public String getEdu_from() {
        return edu_from;
    }

    public void setEdu_from(String edu_from) {
        this.edu_from = edu_from;
    }

    public String getEdu_to() {
        return edu_to;
    }

    public void setEdu_to(String edu_to) {
        this.edu_to = edu_to;
    }
}
